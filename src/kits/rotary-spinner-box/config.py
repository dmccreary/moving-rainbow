# Configuration file for the rotary spinner box
# The box has a 12-pixel NeoPixel strip on GPIO Pin 0, and a rotary encoder on GPIO Pins 12 and 13.
# There are two momentary push buttons on GPIO Pins 14 and 15, which are used to change the mode of the box.
NUMBER_PIXELS = 12
NEOPIXEL_PIN = 0
ROTARY_ENCODER_PIN_A = 12
ROTARY_ENCODER_PIN_B = 13
PUSH_BUTTON_PIN_1 = 14
PUSH_BUTTON_PIN_2 = 15
